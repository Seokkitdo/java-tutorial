package com.kh.homework.employee.view;

import java.util.Scanner;

import com.kh.homework.employee.controller.EmployeeController;

public class EmployeeMenu {
	private Scanner sc = new Scanner(System.in);
	private EmployeeController ec = new EmployeeController();
	
	public EmployeeMenu() {
		int menuNum;
		do {
			System.out.println("1. 사원 추가");
			System.out.println("2. 사원 수정");
			System.out.println("3. 사원 삭제");
			System.out.println("4. 사원 출력");
			System.out.println("9. 프로그램 종료");
			System.out.print("메뉴 번호를 누르세요 : ");
			menuNum = sc.nextInt();
			
			switch(menuNum) {
			case 1: insertEmp(); break;
			case 2: updateEmp(); break;
			case 3: deleteEmp(); break;
			case 4: printEmp(); break;
			case 9: System.out.println("프로그램을 종료합니다."); break;
			default: System.out.println("번호를 잘못 입력하셨습니다. 다시 입력해주세요.");
			}
		} while (menuNum != 9);
		
		
		
	}
	
	public void insertEmp() {
		
		char yn;
		do {
			System.out.print("사원 번호 : ");
			int empNo = sc.nextInt();
			sc.nextLine();
			System.out.print("사원 이름 : ");
			String name = sc.nextLine();
			System.out.print("사원 성별 : ");
			char gender = sc.nextLine().charAt(0);
			System.out.print("전화 번호 : ");
			String phone = sc.nextLine();
			System.out.print("추가 정보를 더 입력하시겠습니까? (y/n)");
			yn = sc.nextLine().charAt(0);
			if(yn == 'n') {
				ec.add(empNo, name, gender, phone);
			} else if(yn == 'y') {
				System.out.print("사원 부서 : ");
				String dept = sc.nextLine();
				System.out.print("사원 연봉 : ");
				int salary = sc.nextInt();
				System.out.print("보너스 율 : ");
				double bonus = sc.nextDouble();
				ec.add(empNo, name, gender, phone, dept, salary, bonus);
			
			}
		}while(!(yn == 'n' || yn == 'y'));
	}
	
	public void updateEmp() {
		int menuNum = 0;
		do {
			System.out.println("가장 최신의 사원 저오를 수정하게 됩니다");
			System.out.println("사원의 어떤 정보를 수정하시겠습니까?");
			System.out.println("1. 전화 번호");
			System.out.println("2. 사원 연봉");
			System.out.println("3. 보너스 율");
			System.out.println("9. 돌아가기");
			System.out.print("메뉴 번호를 누르세요 : ");
			
			switch(menuNum) {
			case 1:
				System.out.print("수정할 전화번호 : ");
				String phone = sc.nextLine();
				ec.modify(phone);
				break;
			case 2:
				System.out.print("수정할 연봉 : ");
				int salary = sc.nextInt();
				ec.modify(salary);
				break;
			case 3:
				System.out.print("수정할 보너스율 : ");
				double bonus = sc.nextDouble();
				break;
			case 9:
				return;
			default :
				System.out.println("올바른 번호를 눌러주세요");	
			}
		}while(menuNum != 9);
	}
	public void deleteEmp() {
		char yn;
			System.out.println("정말 삭제하시겠습니까? (y/n)");
			sc.nextLine();
			yn = sc.nextLine().toUpperCase().charAt(0);
			if(yn == 'Y') {
				ec.remove();
				
			} 
		
	}
	
	public void printEmp() {
		String result = ec.toString();
		
		if(result == null) {
			System.out.println("존재하는 사원 데이터가 없습니다.");
		} else {
			System.out.println("사번\t이름\t성별\t핸드폰번호\t부서\t급여\t보너스");
			
			System.out.println(result);
		}
	}
	
}
